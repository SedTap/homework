// Задача 9 
// Вам дано случайное неотрицательное число, и вы должны вернуть его цифры в обратном порядке в виде массива.

// Пример (ввод => вывод):
// 35231 => [1,3,2,5,3]
// 0     => [0]

function digitize(n) {
  return String(n).split('').reverse().map(Number);
}

