// Задача 4
// Часы показывают h часов, m минут и s секунд после полуночи.
// Ваша задача — написать функцию, которая возвращает время, прошедшее с полуночи, в миллисекундах.

function past(h, m, s) {
  // 1 час = 3600000 миллисекунд (60 * 60 * 1000)
  // 1 минута = 60000 миллисекунд (60 * 1000)
  // 1 секунда = 1000 миллисекунд
  
  return h * 3600000 + m * 60000 + s * 1000;
}

// Задача 5 
//Вам дана строка, и вы должны вернуть строку, в которой каждый символ (с учетом регистра) повторяется один раз.
// Примеры (ввод -> вывод):
// * "String"      -> "SSttrriinngg"
// * "Hello World" -> "HHeelllloo  WWoorrlldd"
// * "1234!_ "     -> "11223344!!__  "

function doubleChar(str) {
  const result = str.split('').map(char => char + char).join('');
  console.log(`Вход: "${str}" → Выход: "${result}"`);
  return result;
}

// Проверка
doubleChar("String");        // "SSttrriinngg"
doubleChar("Hello World");   // "HHeelllloo  WWoorrlldd"
doubleChar("1234!_ ");       // "11223344!!__  "
doubleChar("abc");           // "aabbcc"
doubleChar("");              // ""

